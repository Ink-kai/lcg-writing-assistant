export function getFieldLabel(field: FrontmatterFieldDefinition): string {
	const key = `field.label.${field.key}` as TranslationKey;
	return t(key);
}

export function getFieldDescription(field: FrontmatterFieldDefinition): string {
	const key = `field.desc.${field.key}` as TranslationKey;
	return t(key);
}

export function getGroupLabel(group: FrontmatterFieldGroup): string {
	const key = `field.group.${group}` as TranslationKey;
	return t(key);
}

export function getSourceLabel(source: FrontmatterFieldSource): string {
	const key = `field.source.${source}` as TranslationKey;
	return t(key);
}

