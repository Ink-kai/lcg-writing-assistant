export const REQUIRED_FIELD_KEYS = LCG_FRONTMATTER_FIELDS
	.filter((field) => field.required)
	.map((field) => field.key);

export const ARRAY_FIELD_KEYS = LCG_FRONTMATTER_FIELDS
	.filter((field) => field.type === "string[]")
	.map((field) => field.key);

export const COMPATIBILITY_FIELD_KEYS = LCG_FRONTMATTER_FIELDS
	.filter((field) => field.deprecated)
	.map((field) => field.key);

export function getFieldDefinition(key: string): FrontmatterFieldDefinition | undefined {
	return LCG_FRONTMATTER_FIELDS.find((field) => field.key === key);
}
